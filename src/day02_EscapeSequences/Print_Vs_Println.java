package day02_EscapeSequences;

public class Print_Vs_Println {
    public static void main(String[] args) {

        System.out.println("Hello everyone");
        System.out.println("How are you all today?");
        System.out.println("------------------------");

        System.out.print("Hello everyone ");
        System.out.print("How are you all today?");

        System.out.println("Java Programming");

        System.out.print("Wooden Spoon ");

        System.out.println("I love you  Java");

        System.out.println("I love you Data Structure");
    }
}
